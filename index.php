    <h1>Bench Press Form Guide</h1>
    <ol>
        <li>
            <strong>Setup:</strong>
            <ul>
                <li>Lie flat on a bench with your eyes directly under the barbell.</li>
                <li>Plant your feet firmly on the ground.</li>
                <li>Grip the barbell with your hands pronated (palms facing away) and slightly wider than shoulder-width apart.</li>
                <li>Wrap your thumbs around the bar.</li>
            </ul>
        </li>
        <li>
            <strong>Tuck Your Shoulder Blades:</strong>
            <ul>
                <li>Tuck your shoulder blades down and back.</li>
                <li>Lift your chest towards the ceiling to create a stable base.</li>
            </ul>
        </li>
        <li>
            <strong>Unrack the Bar:</strong>
            <ul>
                <li>Take a big breath and unrack the bar by straightening your arms.</li>
                <li>Move the bar over your shoulders with your elbows locked.</li>
            </ul>
        </li>
        <li>
            <strong>Lower the Bar:</strong>
            <ul>
                <li>Lower the bar to your mid-chest while tucking your elbows about 75Â°.</li>
                <li>Avoid flaring your elbows out to 90Â°, as this can strain your shoulders.</li>
            </ul>
        </li>
        <li>
            <strong>Press the Bar Back Up:</strong>
            <ul>
                <li>Press the bar back up until your arms are straight.</li>
                <li>Keep your butt on the bench throughout the movement.</li>
            </ul>
        </li>
        <li>
            <strong>Hold and Breathe:</strong>
            <ul>
                <li>Hold the weight for a second at the top with straight arms.</li>
                <li>Breathe. Then take a big breath, hold it, and lower the bar again.</li>
            </ul>
        </li>
    </ol>
